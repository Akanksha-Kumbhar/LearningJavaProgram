package ex_02_Java_Basics_Variables;

public class Lab09_printF {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.printf("a = %d, b = %d", a,b);
        System.out.println();
        System.out.printf("Value of a is = %d, Value of b is = %d", a,b);
        // %d = int, short, byte, long--data type
        // %s = string
        // %f = float, double
        // %b = boolean
    }
}
