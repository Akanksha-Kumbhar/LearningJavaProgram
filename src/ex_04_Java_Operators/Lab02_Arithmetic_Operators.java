package ex_04_Java_Operators;

public class Lab02_Arithmetic_Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 30;
        float c = 2.2f;
        System.out.println(a+b);
        System.out.println(b-a);
        System.out.println(a%b);
        System.out.println(a*b);
        System.out.println(c/a);
        System.out.println("a+b");

    }
}
