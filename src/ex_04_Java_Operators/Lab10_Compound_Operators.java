package ex_04_Java_Operators;

public class Lab10_Compound_Operators {
    public static void main(String[] args) {
        int age = 25;
        age += 10;//age = age + 10; // += compound operators // 35
        age /= 5; // 7
        age -= 2; // 5
        age *= 2; // 10

        System.out.println(age); // 10

    }
}
